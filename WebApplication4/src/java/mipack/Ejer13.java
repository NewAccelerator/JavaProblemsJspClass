public class Ejer13 {   
            
    public static double dobleN(double numero) {
        double doble = numero * 2;
        return doble;
    }
    public static double tripleN(double numero) {
        double triple = numero*3;
        return triple;
    }
    public static double cuadradoN(double numero) {
        double cuadrado = numero*numero;
        return cuadrado;
    }
    public static double cuboN(double numero) {
        double cubo = numero*numero*numero;
        return cubo;
    }
     
}
